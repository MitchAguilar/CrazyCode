package MotorLogico;

public class Planta {
    private String Color;
    private float Tamaño;
    private double precio;

    public Planta(String Color, float Tamaño, double precio) {
        this.Color = Color;
        this.Tamaño = Tamaño;
        this.precio = precio;
    }
    public Planta() {
        this.Color = Color;
        this.Tamaño = Tamaño;
        this.precio = precio;
    }
    
    
    
    
    
}
